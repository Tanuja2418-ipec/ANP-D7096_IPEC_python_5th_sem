""" Write a Python program to find the average mileage of a car. """

"Coding Section"


Total_distance_travelled = float(input("Enter the the total distance travelled(In KM): "))
Fuel_consumed = float(input("Enter the fuel consumed(In litres): "))


Mileage = Total_distance_travelled / Fuel_consumed

"output"

print( "Total Mileage (In KM/L) is :", Mileage)