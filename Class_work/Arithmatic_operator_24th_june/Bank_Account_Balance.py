""" Write a Python program to calculate the remaining balance after withdrawal. """

"Coding Section"


Current_Balance = float(input("Enter the Current Amount(in rupees): "))
Withdrawal_Amount = int(input("Enter the amount to withdraw (in rupees): "))

Remaining_Balance = Current_Balance - Withdrawal_Amount 


"Output"

print("Remaining Balance is :  Rs ",Remaining_Balance,)